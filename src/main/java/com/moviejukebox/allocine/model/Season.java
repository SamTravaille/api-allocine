package com.moviejukebox.allocine.model;

import com.fasterxml.jackson.annotation.JsonProperty;
import com.fasterxml.jackson.annotation.JsonRootName;
import java.util.ArrayList;
import java.util.List;

@JsonRootName("season")
public class Season extends AbstractJsonUnknownHandleMapping {

    private static final long serialVersionUID = -9173083320025101754L;
    
    @JsonProperty("code")
    private int code;
    @JsonProperty("seasonNumber")
    private int seasonNumber;
    @JsonProperty("yearStart")
    private int yearStart;
    @JsonProperty("yearEnd")
    private int yearEnd;
    @JsonProperty("episodeCount")
    private int episodeCount;
    @JsonProperty("episode")
    private List<Episode> episodeList = new ArrayList<Episode>();

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public int getSeasonNumber() {
        return seasonNumber;
    }

    public void setSeasonNumber(int seasonNumber) {
        this.seasonNumber = seasonNumber;
    }

    public int getYearStart() {
        return yearStart;
    }

    public void setYearStart(int yearStart) {
        this.yearStart = yearStart;
    }

    public int getYearEnd() {
        return yearEnd;
    }

    public void setYearEnd(int yearEnd) {
        this.yearEnd = yearEnd;
    }
    
    public int getEpisodeCount() {
        return episodeCount;
    }

    public void setEpisodeCount(int episodeCount) {
        this.episodeCount = episodeCount;
    }

    public List<Episode> getEpisodeList() {
        return episodeList;
    }

    public void setEpisodeList(List<Episode> episodeList) {
        this.episodeList = episodeList;
    }
}
