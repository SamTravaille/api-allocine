package com.moviejukebox.allocine.model;

import com.fasterxml.jackson.annotation.JsonProperty;
import com.fasterxml.jackson.annotation.JsonRootName;

@JsonRootName("medium")
public class Medium extends AbstractJsonMapping {

    private static final long serialVersionUID = 4649776514511001593L;
    private static final long POSTER_MEDIA_CODE = 31001;

    @JsonProperty("type")
    private Type type;
    @JsonProperty("title")
    private String title;
    @JsonProperty("thumbnail")
    private Thumbnail thumbnail;
    @JsonProperty("description")
    private String description;
    @JsonProperty("width")
    private long width;
    @JsonProperty("height")
    private long height;

    public Type getType() {
        return type;
    }

    public void setType(Type type) {
        this.type = type;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public Thumbnail getThumbnail() {
        return thumbnail;
    }

    public void setThumbnail(Thumbnail thumbnail) {
        this.thumbnail = thumbnail;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public long getWidth() {
        return width;
    }

    public void setWidth(long width) {
        this.width = width;
    }

    public long getHeight() {
        return height;
    }

    public void setHeight(long height) {
        this.height = height;
    }

    public boolean isPoster() {
        if (this.type != null) {
            return this.type.getCode() == POSTER_MEDIA_CODE;
        }
        return false;
    }
}
